# 12. Leia o salário de um trabalhador e escreva seu novo salário com um aumento de 25%.

# Entrada
salario = float(input('Quanto o trabalhador ganha:'))

# Processamento
novo_salario = salario * 1.25

# Saída
print(f'o salário de {salario:.2f} com um aumento de 25%, se tornará {novo_salario:.2f}!')
