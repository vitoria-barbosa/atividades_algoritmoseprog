# 15. Leia o valor da base e altura de um triângulo, calcule e escreva sua área. (área=(base * altura)/2)

# Entrada
base = float(input('Base do triângulo:'))
altura = float(input('Altura do triângulo:'))

# Processamento
area = (base * altura) / 2

# Saída
print(f'A área desse triângulo é {area:.1f}')
