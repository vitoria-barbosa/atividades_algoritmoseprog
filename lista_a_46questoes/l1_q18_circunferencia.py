# 18. Leia o valor do raio de uma circunferência, calcule e escreva seu comprimento.(c = 2 * p * r)

# Entrada
raio = float(input('Valor do raio da circunferência:'))

# Processamento
comprimento = (2 * 3.14 * raio)

# Saída
print(f'O comprimento dessa circunferência é = {comprimento:.1f}')
